/**
 * @brief      This function allows to print basic information on LCD screen
 * @param      ucText  Text you want to display on LCD
 * @param[in]  eError  Depending on error code a different kind of message is shown on the LCD (OK, FAILURE)
 */
static void prvLcdLog( uint8_t *ucText, Error_t eError ,Position_t ePosition )
{
	uint32_t ulColor = 0;
	uint16_t *pusFont = 0;
	uint8_t ucTextPostition = 0;

	//Switch case to determine how to display text color
	switch( eError )
	{
		case OK: ulColor = LCD_COLOR_BLACK; break;
		case FAILURE: ulColor = LCD_COLOR_RED; break;
		default: prvLcdLog("There isn't such error",FAILURE,BOTTOM); break;
	}

	//Switch case to determine how to display information on the LCD
	switch ( ePosition)
	{
		case TOP:
			 pusFont = &Font24;
			 ucTextPostition = LCD_Y_SPACING;
			 prvLcdClearLine(0,5);
			break;
		case MID:
			 pusFont = &Font24;
			 ucTextPostition = LCD_CENTER_HORIZONTAL;
			 prvLcdClearLine(5,10);
			break;
		case BOTTOM:
			pusFont = &Font12;
			ucTextPostition = LCD_CENTER_BOTTOM;
			prvLcdClearLine(20,25);
			break;
		case P_TEMPERATURE:
			pusFont = &Font24;
			ucTextPostition = ( LCD_SPACING * 2 ) + LCD_Y_SPACING;
			break;
		case P_WEATHER:
			pusFont = &Font24;
			ucTextPostition = ( LCD_SPACING * 3 ) + LCD_Y_SPACING;
			break;
		case P_HUMANITY:
			pusFont = &Font24;
			ucTextPostition = ( LCD_SPACING * 4 ) + LCD_Y_SPACING;
			break;
		case P_CITY:
			pusFont = &Font24;
			ucTextPostition = ( LCD_SPACING * 5 ) + LCD_Y_SPACING;
			break;
		default: prvLcdLog("There isn't such LCD position",FAILURE,BOTTOM);break;
	}

	//Display selected information on LCD
	BSP_LCD_SetBackColor( LCD_COLOR_WHITE );
	BSP_LCD_SetTextColor( ulColor );
	BSP_LCD_SetFont( pusFont );
	BSP_LCD_DisplayStringAt( 0, ucTextPostition, ( uint8_t* )ucText , CENTER_MODE );
}
