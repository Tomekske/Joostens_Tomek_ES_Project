int main(void)
{

  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration----------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* Configure the system clock */
  SystemClock_Config();

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_FMC_Init();
  MX_I2C1_Init();
  MX_I2C3_Init();
  MX_LTDC_Init();
  MX_QUADSPI_Init();
  MX_USART1_UART_Init();
  MX_USART6_UART_Init();
  MX_DMA2D_Init();
  MX_LWIP_Init();

  /* USER CODE BEGIN 2 */

  /* LCD Initialization */
    BSP_LCD_Init();
    BSP_LCD_LayerDefaultInit( 0, LCD_FB_START_ADDRESS );
    BSP_TS_Init( LCD_HEIGHT, LCD_LENGHT );

    /* Enable the LCD */
    BSP_LCD_DisplayOn();

    /* Select the LCD Background Layer  */
    BSP_LCD_SelectLayer( 0 );

    /* Clear the Background Layer */
    BSP_LCD_Clear( LCD_COLOR_WHITE );

    /* Some sign */
    BSP_LCD_SetTextColor( LCD_COLOR_BLACK );
    BSP_LCD_SetFont( &Font24 );
    BSP_LCD_ClearStringLine( 0 );

    TS_StateTypeDef TS_State;
    BSP_TS_GetState(&TS_State);
    volatile uint32_t ulCounter = 0;
    uint8_t ucTouchScreen = 0, ucShowOnce = 0,ucBegin= 0;

	//wait until TS is pressed    
    while( TS_State.touchDetected == FALSE )
    {
  	  BSP_TS_GetState( &TS_State ); //Fetch for TS's state

  	  //display only text once, this is needed due in the function prvLcdLog the LCD lines are getting cleared
  	  if( ucShowOnce == FALSE )
  	  {
  		  ucShowOnce = TRUE;
  		  prvLcdLog( "Please touch the screen", OK, MID );
  	  }

  	  else
  	  {
  		  //Do nothing
  	  }
    }
    BSP_LCD_Clear( LCD_COLOR_WHITE ); //After TS is pressed, the screen is being cleared

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while ( 1 )
  {
  /* USER CODE END WHILE */

  /* USER CODE BEGIN 3 */
	BSP_TS_GetState( &TS_State );    //Fetch for TS's state
	MX_LWIP_Process();

	 //First time show
	if( ucBegin == FALSE && ulCounter++ > 500 )
	{
		ucBegin = TRUE;
		prvFetchData();
	}

	//Fetch data after the data is shown for the fist time and when TS is pressed
	if( TS_State.touchDetected == TRUE && ucTouchScreen == FALSE && ucBegin == TRUE )
	{
		ucTouchScreen = TRUE;
		BSP_LCD_Clear( LCD_COLOR_WHITE );

		while( ulCounter < 500 )
		{
			ulCounter++;
		}

		prvFetchData();
	}

	//If TS is not pressed reset all values, so TS can be pressed again
	else if( TS_State.touchDetected == FALSE && ucTouchScreen == TRUE )
	{
		ucTouchScreen = FALSE;
		ulCounter = FALSE;
	}

	else
	{
		//do nothing
	}

	HAL_Delay(1);
  }
  /* USER CODE END 3 */
}

