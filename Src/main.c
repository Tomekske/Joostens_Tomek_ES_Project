/**
 * @brief      This function allows us to search for data in the array of string
 * @param      ucDestination  This string stores the found data
 * @param      pucContent     The filtered array of strings you want to search for data
 * @param[in]  ucLenght       Pass the amount amount of items in array of strings
 * @param[in]  xSearch        Pass what you want to search as argument (COUNTRY, CITY, WEATHER, TEMPERATURE, HUMANITY)
 */
void prvGetContent( uint8_t ucDestination[], uint8_t **pucContent, uint8_t ucLenght, Search_t eSearch )
{
	uint8_t ucSearch[LEN] = "";

	//coppy correct search query to ucSearch variable
	switch( eSearch )
	{
		case COUNTRY: strcpy( ucSearch, "country" ); break;
		case CITY: strcpy( ucSearch, "name" ); break;
		case WEATHER: strcpy( ucSearch, "main" ); break;
		case TEMPERATURE: strcpy( ucSearch, "temp" ); break;
		case HUMANITY: strcpy( ucSearch, "humidity" ); break;
		default: prvLcdLog("There isn't such search request",FAILURE,BOTTOM); break;
	}

	//Search for item in array of string
	for( uint8_t i = 0; i < ucLenght; i++ )
	{
		//item found
		if( strcmp( pucContent[i], ucSearch ) == 0 )
		{
			strcpy( ucDestination, pucContent[i + 1] ); //data of searched item is one index above the index of the item
			break; //if item is found we exit the loop incase there are multiple items of the same name.
		}

		//We continue our search through the array of strings
		else
		{
			continue;
		}
	}
}
