/**
 * @brief      This function allows to filter the fetched JSON string with the weather data, all special characters are removed and stored in an array of strings
 * @param      pucDestination  The filtered string is stored in this array of strings
 * @param      ucSource        The unfilterd string is passed in this parameter
 * @param      ucFilter        A string which allows you to pass in all characters you want to filter out
 * @return     Amount of items in array of strings
 */
uint8_t prvFilterContent( uint8_t **pucDestination, uint8_t ucSource[], uint8_t ucFilter[] )
{
	uint8_t ucCounter = 0;
	uint8_t *pucTemporary = strtok( ucSource, ucFilter ); //Get first token

	//Get other tokens until end of string
	while( pucTemporary != NULL )
	{
		pucDestination[ucCounter] = pucTemporary; //copy token into array of strings
		pucTemporary = strtok( NULL, ucFilter ); //get another token, NULL indicates its getting a token from the same string
		ucCounter++;
	}
	return ucCounter;
}
