/**
 * @brief      Callback function is called when the data from the server is received
 * @param      arg   Pass own arguments
 * @param      tpcb  Protocol control blocks (PCBs) are used at the protocol layer to hold the various pieces of information required for each TCP socket.
 * @param      p     buffer which hold all the information of received data
 * @param[in]  err   What kind of error
 * @return     returns error code that everything is ok
 */
err_t prvDataReceived( void *arg, struct tcp_pcb *tpcb, struct pbuf *p, err_t err )
{
	char xBuffer[700];
    Weather_t xWeather;
    int8_t ucFilter[LEN] = "<>{}\"\": ,[]'`"; //String which characters you want to filter
	uint8_t ucCounter = 0; //Counter to count amount of items in array of strings
	uint8_t **pucFiltered = (uint8_t*)malloc(sizeof(uint8_t)); //Dynamically alocted array of strings
	uint8_t ucTitle[16] = "Weather Station";
	uint8_t ucCompleteTemperature[32] = "", ucCompleteCountry[32] = "", ucCompleteHumanity[32] = "";

	//if data transmission is completed the connection has to be closed.
    if( p == NULL && !err )
    {
        tcp_close( tpcb );
        return ERR_OK;
    }

    //Filter all the received data
    else
    {
		ucCounter = prvFilterContent( pucFiltered, p->payload, ucFilter ); //filter array and return amount of characters stored in array of strings

		//Search data in array of strings and store them in a struct variable
		prvGetContent( xWeather.ucCountry, pucFiltered, ucCounter, COUNTRY );
		prvGetContent( xWeather.ucTemp, pucFiltered, ucCounter, TEMPERATURE );
		prvGetContent( xWeather.ucCity, pucFiltered, ucCounter, CITY );
		prvGetContent( xWeather.ucWeather, pucFiltered, ucCounter, WEATHER );
		prvGetContent( xWeather.ucHumanity, pucFiltered, ucCounter, HUMANITY );

		//Add extra information to received data, this is stored in a new string
		sprintf( ucCompleteTemperature, "%s *C", xWeather.ucTemp );
		sprintf( ucCompleteCountry ,"%s, %s", xWeather.ucCity,xWeather.ucCountry );
		sprintf( ucCompleteHumanity ,"%s %%", xWeather.ucHumanity );

		//Display everything on the LCD
		prvLcdLog( "Weather Station", OK,TOP );
		prvLcdLog( ucCompleteTemperature, OK, P_TEMPERATURE );
		prvLcdLog( ucCompleteHumanity, OK, P_HUMANITY );
		prvLcdLog(xWeather.ucWeather, OK, P_WEATHER );
		prvLcdLog( ucCompleteCountry, OK, P_CITY );

		//iets doen met de data
		tcp_recved( tpcb, p->len );

		pbuf_free( p );

		return ERR_OK;
	}
}
